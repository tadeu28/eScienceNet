    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        
        if (tblClasses.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(this, "Is mandatory select some class.", ".: e-ScienceNet :.", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        OWLClassesModel classModel = new OWLClassesModel();
        classModel.setSuperClass(this.superClass);
        classModel.setName(tblClasses.getModel().getValueAt(tblClasses.getSelectedRow(), 0).toString());
        classModel.setUri(tblClasses.getModel().getValueAt(tblClasses.getSelectedRow(), 1).toString());
        
        this.owlClassesModel = classModel;
        
        this.setVisible(false);
    }//GEN-LAST:event_btnOKActionPerformed

