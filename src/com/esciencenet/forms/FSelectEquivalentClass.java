    private void preencherGrid(StringTokenizer owlClasses, StringTokenizer owlURIs){
        
        DefaultTableModel tblModel = (DefaultTableModel) tblClasses.getModel();
        
        while(owlClasses.hasMoreTokens()){
            
            Object[] linha = {owlClasses.nextToken().trim(), owlURIs.nextToken().trim()};
            
            tblModel.addRow(linha);
        }        
    }
    
