    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
                
        String domainName = tblArquivos.getModel().getValueAt(tblArquivos.getSelectedRow(), 0).toString();
        String fileName = tblArquivos.getModel().getValueAt(tblArquivos.getSelectedRow(), 1).toString();
        
        if (JOptionPane.showConfirmDialog(this, "Do you really want to delete the domain "+ domainName +"?", "e-ScienceNet", 
            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.OK_OPTION){

            if (!SemanticManager.getInstance().removerDomainInOWL(domainName)){
                JOptionPane.showMessageDialog(this, "Wasn't possible to remove the domain in PeerOntology.", "e-ScienceNet", JOptionPane.ERROR_MESSAGE);
            }else{
                File fileOWL = new File(fileName);
                if (fileOWL.exists()){
                    fileOWL.delete();
                }

                this.removeDomain(domainName);

                if (cbmGrupos.getSelectedItem().toString().equals("")){
                    this.exibirDominios();
                }else{
                    exbirDominiosSobreFiltro(cbmGrupos.getSelectedItem().toString());
                }    
            }
        }
            
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void removeDomain(String domainName){
        
        for(DomainFileModel domainFileModel : lstDomainFileModels){
            
            if (domainFileModel.getDomainName().equals(domainName)){
                lstDomainFileModels.remove(domainFileModel);
                break;
            }
        }
    }
    
