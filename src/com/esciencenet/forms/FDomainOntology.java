    private void exbirDominiosSobreFiltro(String filtro){
      
        limparTabela();
        
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblArquivos.getModel();
        
        for (DomainFileModel domain : lstDomainFileModels){
        
            if (domain.getGroup().getGroupName().equals(filtro)){
                Object[] linha = {domain.getDomainName(),                               
                                  domain.getDomainOWLFile()};

                defaultTableModel.addRow(linha);
            }
        }           
    }
    
    private void exibirDominios(){       
        
        limparTabela();
        
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblArquivos.getModel();
        
        for (DomainFileModel domain : lstDomainFileModels){
        
            Object[] linha = {domain.getDomainName(),                               
                              domain.getDomainOWLFile()};
            
            defaultTableModel.addRow(linha);
        }        
    }
    
