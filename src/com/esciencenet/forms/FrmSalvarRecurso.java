    public FrmSalvarRecurso(java.awt.Frame parent, boolean modal, String diretorioPreferencial, GerenciaProcura gerenciaProcura,
                            ConteudoRemoto conteudoRemoto, ContentAdvertisement adv, String name) {
        super(parent, modal);
        
        initComponents();
        
        if (System.getProperty("os.name").toLowerCase().equals("linux")){
            this.setType(Type.UTILITY);
        }else{
            this.setType(Type.POPUP);
        }
        
        this.diretorioPreferencia = diretorioPreferencial;
        this.gerenciaProcura = gerenciaProcura;
        this.conteudoRemoto = conteudoRemoto;
        
        setLocationRelativeTo(null);
        this.setResult(adv, name);
        
        try {
            if (fileName != null){
                PreferenceReader prefs = PreferenceReader.getInstance();
                String dir = (new File(fileName)).getParent();
                prefs.setProperty(getClass().getName() + ConteudoRemoto.DIRETORIO_PREFERENCIA, dir);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

