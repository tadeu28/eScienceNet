    private void btnDiretorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDiretorioActionPerformed
        FileDialog fd = new FileDialog(this, "Salvar Conte√∫do", FileDialog.SAVE);
        String cwd = diretorioPreferencia;
        fd.setFile(name);
        fd.setDirectory(cwd);
        fd.show();
        setEnabled(true);
        
        if (fd.getDirectory() != null && fd.getFile() != null) {
            fileName = fd.getDirectory() + fd.getFile();
            edtArquivo.setText(fileName);
        }
    }//GEN-LAST:event_btnDiretorioActionPerformed

