    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try{
            File file = new File(fileName);
            if(file.exists()){
                
                file.delete();
                file.createNewFile();
                
                if(file.exists()){
                    try (FileWriter fw = new FileWriter(file)) {
                        fw.write(EScienceNetUtils.encodeBase64(edtResults.getText()));
                        JOptionPane.showMessageDialog(null, "The file was save with success!", ".: e-ScienceNet :.", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

