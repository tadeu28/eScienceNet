    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        
        try{
            File file = new File(this.fileName);
            if(!file.exists()){
                throw new Exception("The file don't exists.");
            }
            
            String contenct = EScienceNetUtils.readFileToString(file, false);
            
            edtResults.setText(EScienceNetUtils.decodeBase64(contenct));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage(), ".: e-ScienceNet :.", JOptionPane.ERROR_MESSAGE);
            this.setVisible(false);
        }        
    }//GEN-LAST:event_formComponentShown

