    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        //crio o modelo de grupo
        PeerGroupModel peerGroup = new PeerGroupModel();      
        
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy"); 
        
        //seto as informaçoes previas que o formulario disponibiliza
        peerGroup.setGroupArea(edtArea.getText());
        peerGroup.setGroupDescription(edtDescription.getText());
        peerGroup.setGroupName(edtNomeGrupo.getText());
        peerGroup.setDataCriacao(formato.format(new Date()));
        
        //salvo o grupo na ontologia
        SemanticManager.getInstance().gravarGrupoInOWL(peerGroup, true);
        
        //fecho a tela de grupos
        this.setVisible(false);
    }//GEN-LAST:event_btnSalvarActionPerformed

